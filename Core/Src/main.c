/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_GPDMA1_Init();
  MX_SAI1_Init();
  MX_FileX_Init();
  MX_RTC_Init();
  MX_DCACHE1_Init();
  MX_ICACHE_Init();
  /* USER CODE BEGIN 2 */
  MX_FileX_Process();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* for audio */
    switch(wave_conf.status)
    {
      case INIT:
        HAL_SAI_Receive_DMA(&hsai_BlockA1, (uint8_t *)wave_conf.recod_buff, WAV_RECORD_BUFF_SIZE);
        wave_conf.status = START;
        wave_conf.file_number=0;
        break;

      case START:
        wave_conf.file_number++;
        wave_conf.saved_file_size = 0;
        get_filename(wave_conf.filename, sizeof(wave_conf.filename), wave_conf.file_number, wave_conf.file_type);
        wave_file_create(wave_conf.filename, &wave_conf);
        wave_conf.status = UPDATE;
        break;

      case UPDATE:
        if(wave_conf.update_pointer != -1)
        {
          if(wave_file_update(wave_conf.filename, (uint8_t *)&wave_conf.recod_buff[wave_conf.update_pointer], (int)((WAV_RECORD_BUFF_SIZE/2) * 4)) == 1)
          {
            wave_conf.status = STOP;
          }
          wave_conf.update_pointer = -1;
        }
        break;

      case STOP:
        wave_file_close(wave_conf.filename);
        wave_conf.status = START;
        break;
    }

    /* for csv */
    switch(csv_conf.status)
    {
      case INIT:
        csv_conf.status = START;
        csv_conf.file_number=0;
        break;

      case START:
        csv_conf.file_number++;
        csv_conf.saved_file_size = 0;
        get_filename(csv_conf.filename, sizeof(csv_conf.filename), csv_conf.file_number, csv_conf.file_type);
        csv_conf.status = UPDATE;
        break;

      case UPDATE:
        if(csv_conf.update_100hz == 1)
        {
          if(csv_file_update(csv_conf.filename, (uint8_t *)&csv_conf.recod_buff, (int)(CSV_RECORD_BUFF_SIZE)) == 1)
          {
            csv_conf.status = STOP;
          }
          csv_conf.update_100hz = 0;
        }
        break;

      case STOP:
        csv_file_close(csv_conf.filename);
        csv_conf.status = START;
        break;
    }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

