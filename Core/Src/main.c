static void get_filename(char *filename, int size, int number, filetype_e filetype)
{
  RTC_TimeTypeDef sTime;
  RTC_DateTypeDef sDate;

  // RTC 현재 시간 가져오기
  if (HAL_RTC_GetTime(&hrtc, &sTime, RTC_FORMAT_BIN) != HAL_OK)
  {
    Error_Handler();
  }

  // RTC 현재 날짜 가져오기
  if (HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN) != HAL_OK)
  {
    Error_Handler();
  }

  memset(filename, 0x00, size);

  if(filetype == WAV_FILE)
  {
    snprintf(filename, size, "%04d%02d%02d_%02d%02d%02d_%04d.wav",
             2000 + sDate.Year, sDate.Month, sDate.Date,
             sTime.Hours, sTime.Minutes, sTime.Seconds, number);
  }
  else if(filetype == CSV_FILE)
  {
    snprintf(filename, size, "%04d%02d%02d_%02d%02d%02d_%04d.csv",
             2000 + sDate.Year, sDate.Month, sDate.Date,
             sTime.Hours, sTime.Minutes, sTime.Seconds, number);
  }
}
